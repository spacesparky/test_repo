public class Fitcalk {
    public static float fitCalc(int x, int y) {
        return x * y;
    }
    public static void main(String[] args) {
        // Examples of using the fitCalc function
        System.out.println(fitCalc(15, 1)); // ➞ 15
        System.out.println(fitCalc(24, 2)); // ➞ 48
        System.out.println(fitCalc(41, 3)); // ➞ 123
    }
}
